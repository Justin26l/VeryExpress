// read package.json and get version
import fs from "fs";
import path from "path";
import * as types from "../types/types";
import log from "./logger";
import * as generator from "./generator";
import pkg from "package.json";

// files written (or skipped due to no-change / allowOverwrite=false) during current generation run
export const writtedFiles: string[] = [];
export let _compilerOptions: types.compilerOptions = generator.defaultCompilerOptions;
let _vexMeta: types.VexMeta = { files: {} };

export function resetWrittedFiles(): void { writtedFiles.length = 0; }

function vexMetaPath(): string {
    return path.posix.join(_compilerOptions?.rootDir || ".", ".vex", "meta.json");
}

function flatToNested(flat: { [relPath: string]: types.VexFileMeta }): Record<string, any> {
    const root: Record<string, any> = {};
    for (const [relPath, meta] of Object.entries(flat)) {
        const parts = relPath.split("/");
        let node = root;
        for (let i = 0; i < parts.length - 1; i++) {
            node[parts[i]] = node[parts[i]] || {};
            node = node[parts[i]];
        }
        node[parts[parts.length - 1]] = meta;
    }
    return root;
}

function nestedToFlat(nested: Record<string, any>, prefix = ""): { [relPath: string]: types.VexFileMeta } {
    const flat: { [relPath: string]: types.VexFileMeta } = {};
    for (const [key, value] of Object.entries(nested)) {
        const relPath = prefix ? `${prefix}/${key}` : key;
        if (value && typeof value === "object" && !("lastWriteVersion" in value)) {
            Object.assign(flat, nestedToFlat(value, relPath));
        }
        else {
            flat[relPath] = value as types.VexFileMeta;
        }
    }
    return flat;
}

function flushVexMeta(): void {
    try {
        const metaPath = vexMetaPath();
        const metaDir = path.posix.dirname(metaPath);
        if (!fs.existsSync(metaDir)) fs.mkdirSync(metaDir, { recursive: true });
        const output = {
            lastGeneratedVersion: _vexMeta.lastGeneratedVersion,
            files: flatToNested(_vexMeta.files),
        };
        fs.writeFileSync(metaPath, JSON.stringify(output, null, 2), "utf8");
    }
    catch (err: any) {
        log.warn(`flushVexMeta error: ${err.message}`);
    }
}

function loadVexMeta(): void {
    const metaPath = vexMetaPath();
    try {
        if (fs.existsSync(metaPath)) {
            const raw = JSON.parse(fs.readFileSync(metaPath, "utf8") || "{}");
            _vexMeta = {
                lastGeneratedVersion: raw.lastGeneratedVersion,
                files: nestedToFlat(raw.files || {}),
            };
        }
        else {
            _vexMeta = { files: {} };
        }
    }
    catch {
        _vexMeta = { files: {} };
    }
}

const headerComment: string = generator.getDefaultHeaderComment(pkg.version);

function normalize (s: string) : string{
    return s.replace(/\r\n/g, "\n") // normalize CRLF -> LF
        .replace(/[ \t]+$/gm, "") // remove trailing spaces/tabs on each line
        .trim(); // remove leading/trailing blank lines
}

function pascalCase(str: string): string {
    return str.replace(/(^\w|_\w)/g, (match) =>  match.replace(/_/, "").toUpperCase());
}

function camelCase(str: string): string {
    return str.replace(/(^\w|_\w)/g, (match) =>  match.replace(/_/, "").toLowerCase());
}



export const relativePath = (fromPath: string, toPath: string): string => {
    return path.relative(fromPath, toPath).replace(/\\/g, "/");
};

export function loadJson<T = any>(filePath: string, fileNotExistHandler?: () => T): T {
    // handle file not found
    const fileExist = fs.existsSync(filePath);
    if ( !fileExist && fileNotExistHandler ){
        return fileNotExistHandler();
    }
    else if ( !fileExist ){
        log.error(`FILE : ${filePath} not found`);
    }
    else{
        try {
            const content = JSON.parse(fs.readFileSync(filePath, "utf8"));
            if (!content) log.error(`FILE JSON : Read Error ${filePath}`);
            else return content; 
        }
        catch (err: any) {
            log.error(`FILE : ${filePath}\n`, err.message);
        }
    }
}

export function writeFile(title: string, destination: string, newContent: string): boolean {
    // read file, check difference, if yes write file
    const destinationDir = path.dirname(destination);
    if (!fs.existsSync(destinationDir)) {
        fs.mkdirSync(destinationDir, { recursive: true });
    }
    const oldContent = fs.existsSync(destination) ? fs.readFileSync(destination, "utf8") : "";

    // remove header comment for comparison
    const headerCommentRegex = /\/\*\*\r?\n \* Generated by veryExpress@[\s\S]*?\*\/\s*/g;
    const oldContentNoHeader = normalize(oldContent.replace(headerCommentRegex, ""));

    const newContentRegex = /^.*({{headerComment}}|\/\/ {{headerComment}}).*$(\r?\n)?/gm;
    const newContentNoHeader = normalize(newContent.replace(newContentRegex, ""));

    if (oldContentNoHeader === newContentNoHeader) {
        log.info(`${title} : "${destination}" No changes`);
        writtedFiles.push(destination);
        return false;
    }
    else {
        const opts = _compilerOptions as types.compilerOptions;
        const relPath = opts && opts.rootDir ? path.relative(opts.rootDir, destination).replace(/\\/g, "/") : destination;

        // allowOverwrite: read from .vex/meta.json, default true
        const metaAllow = _vexMeta.files[relPath]?.allowOverwrite;
        const allowOverwrite = metaAllow !== undefined ? metaAllow : true;

        if (!allowOverwrite && fs.existsSync(destination)) {
            log.info(`${title} : "${destination}" Skip (allowOverwrite=false)`);
            writtedFiles.push(destination);
            return false;
        }

        log.writing(`${title} : "${destination}"`);
        writtedFiles.push(destination);
        newContent = newContent.replace(/\/\/ {{headerComment}}|{{headerComment}}/g, headerComment || "// generated files by very-express");
        fs.writeFileSync(destination, newContent);

        // write per-file meta immediately so user can adjust allowOverwrite between runs
        _vexMeta.files[relPath] = {
            lastWriteVersion: pkg.version,
            allowOverwrite,
        };
        flushVexMeta();

        return true;
    }
}

export function saveVexMeta(): void {
    if (!_compilerOptions?.rootDir) return;
    _vexMeta.lastGeneratedVersion = pkg.version;
    flushVexMeta();
}

export function copyDir(source: string, destination: string, compilerOptions: types.compilerOptions, overwrite?: boolean): void {
    if (!fs.existsSync(destination)) {
        fs.mkdirSync(destination);
    }

    const files: string[] = fs.readdirSync(source);

    for (let i = 0; i < files.length; i++) {
        const fileName = files[i];
        const outputFileName = fileName == "index.ts" ? fileName : fileName.replace(".ts", ".gen.ts");

        const sourcePath = path.posix.join(source,fileName);
        const destinationPath = path.posix.join(destination, outputFileName);            

        // recursive copy dir
        if ( fs.lstatSync(sourcePath).isDirectory() ) {
            copyDir(sourcePath, destinationPath, compilerOptions, overwrite);
        }
        // avoid overwrite
        else if (!overwrite && fs.existsSync(destinationPath)) {
            log.info(`FILE : AVOID OVERWRITE, skip "${destinationPath}"`);
        }
        // check file exist, read it check is same content, if is then skip
        else {
            const newContent = fs.readFileSync(sourcePath, "utf8");
            writeFile(`FILE : ${destinationPath}`, destinationPath, newContent);

        }
    }
}

export function scanDir(dir: string): string[] {
    const files: string[] = [];
    if (!fs.existsSync(dir)) return files;
    for (const entry of fs.readdirSync(dir)) {
        const fullPath = path.posix.join(dir, entry);
        if (fs.lstatSync(fullPath).isDirectory()) {
            files.push(...scanDir(fullPath));
        } else {
            files.push(fullPath);
        }
    }
    return files;
}

export function cleanupStaleFiles(): void {
    const sysDir = _compilerOptions?.sysDir;
    if (!sysDir || !fs.existsSync(sysDir)) return;

    const writtenSet = new Set(writtedFiles.map(f => path.resolve(f)));
    const existing = scanDir(sysDir);

    for (const file of existing) {
        if (!writtenSet.has(path.resolve(file))) {
            log.info(`CLEANUP : remove stale file "${file}"`);
            fs.rmSync(file, { force: true });
        }
    }
}

// add setter/getter to update module-scoped _compilerOptions safely
export function setCompilerOptions(opts: types.compilerOptions) {
    _compilerOptions = opts;
    loadVexMeta();
}

export function getCompilerOptions(): types.compilerOptions {
    return _compilerOptions;
}

export default {
    pascalCase,
    camelCase,
    relativePath,
    copyDir,
    loadJson,
    writeFile,
    setCompilerOptions,
    getCompilerOptions,
    saveVexMeta,
    resetWrittedFiles,
    cleanupStaleFiles,
};